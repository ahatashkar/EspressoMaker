public class ButtonNavigationInfo {

    private String name;
    private String navigatedActivity;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNavigatedActivity() {
        return navigatedActivity;
    }

    public void setNavigatedActivity(String navigatedActivity) {
        this.navigatedActivity = navigatedActivity;
    }
}
